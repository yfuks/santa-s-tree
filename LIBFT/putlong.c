#include "libft.h"

void		ft_putlong(long l)
{
	if (l < 0)
	{
		write(1, "-", 1);
		l = -l;
	}
	if (l > 9)
	{
		ft_putlong(l / 10);
		ft_putlong(l % 10);
	}
	else
	{
		l = l + 48;
		write(1, &l, 1);
	}
}
